const ManageProfile = require("../models/supplierPortal/Manageprofile");

// CREATE
exports.createManageProfile = async (req, res) => {
  try {
    const item = new ManageProfile(req.body);
    await item.save();
    res.status(201).json(item);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
};

// READ ALL
exports.getAllManageProfile = async (req, res) => {
  try {
    const items = await ManageProfile.find();
    res.status(200).json(items);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

// READ ONE
exports.getManageProfileById = async (req, res) => {
  try {
    const item = await ManageProfile.findById(req.params.id);
    if (!item) return res.status(404).json({ error: "Not found" });
    res.status(200).json(item);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

// UPDATE
exports.updateManageProfile = async (req, res) => {
  try {
    const item = await ManageProfile.findByIdAndUpdate(req.params.id, req.body, { new: true });
    if (!item) return res.status(404).json({ error: "Not found" });
    res.status(200).json(item);
  } catch (err) {
    res.status(400).json({ error: err.message });
  }
};

// DELETE
exports.deleteManageProfile = async (req, res) => {
  try {
    const item = await ManageProfile.findByIdAndDelete(req.params.id);
    if (!item) return res.status(404).json({ error: "Not found" });
    res.status(200).json({ message: "Deleted" });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};
